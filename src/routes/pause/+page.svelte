<h3>Be specific, and not about someone’s personality</h3>
<p>Feedback should be about a specific work-related behaviour. Are you considering feeding back about someone’s personality? Feedback about someone’s personality is usually unhelpful, and often destructive. It may feel like a personal attack. They may feel confused, defensive, upset or angry. We are all different and do things in different ways - could this be a learning opportunity for you instead?</p>
