<h2>Bear these things in mind too</h2>

<h3>Make it a dialogue</h3>
<p>Stay caring and curious, and try to make feedback a dialogue. You could ask:</p>
<ul>
	<li>How do you feel about my feedback? or</li>
	<li>What's your perspective? or</li>
	<li>How can I help?</li>
</ul>

<h3>Make it timely</h3>
<p>It helps if the feedback is timely â€“ soon after the situation, so it's fresh in their mind.</p>

<h3>Keep it private</h3>
<p>Feedback is best delivered in private, not in a team setting.</p>

<h3>Can cultures collide?</h3>
<p>
	Receptiveness to the various ways that feedback is delivered can differ across personalities,
	lifestyles, industries, and cultures. Just look at context in cultural communication by Edward T.
	Hall (1959). And these are not often details that we know about the people we work with. There is
	no silver bullet for giving feedback. But if you follow a structure for observation (such as SBI)
	and are genuinely curious about understanding their perspective on things, it can be a learning
	experience for both of you. Be prepared to fail, be prepared to learn about them, and how you both
	communicate with one another.
</p>

<h3>You're responsible for the delivery, not the response</h3>
<p>
	Even well delivered feedback can cause an emotional response. You're not responsible for the
	person's reaction. If they respond defensively, be curious and ask them what's going on for them.
	For guidance you could <a href="{base}/">restart the Feedback Sense Check</a> and click 'Someone's
	responded to my feedback'.
</p>

